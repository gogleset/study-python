# 더하기
print(5 + 2)
# 빼기
print(5 - 2)
# 곱하기
print(5 * 2)
# 나누기
print(5 / 2)
# 나머지
print(5 % 2)
# 몫
print(5 // 2)
# 거듭 제곱
print(5**2)


## 비교 연산자 (비교 대상이 왼쪽 기준)
print(5 > 2)
print(5 >= 2)
print(5 < 2)
print(5 <= 2)
print(5 == 2)
print(5 != 2)


## 논리 연산자
print(3 < 5 and 7 < 5)  # 둘 다 참이라면 참
print(3 < 5 or 7 < 5)  # 둘 중 하나라도 참이면 참
print(not 3 < 5)  # 반전

## 멤버 연산자 in = 포함되어 있어, not in = 포함되어 있지 않아
print("c" in "cat")  # c라는 글자가 cat이라는 단어에 포함되어 있으니까 true
print("c" not in "cat")  # c라는 글자가 cat이라는 단어에 포함되어 있으니까 true인데 반전
