# 형 변화
# bool( ? ) 값이 있으면 True 아니면 False


# 문자 자료형

a = "hello"  # 값이 있음
b = "  "  # 값이 있음
c = ""  # 값이 없음

print(bool(a))  # True
print(bool(b))  # True
print(bool(c))  # False

# 숫자 자료형
d = 1  # 값이 있음
e = -2  # 값이 있음
f = 0  # 값이 없음

print(bool(d))  # True
print(bool(e))  # True
print(bool(f))  # False

# 참이면 True, 거짓이면 False
# 값이 있으면 True, 값이 없으면 False
