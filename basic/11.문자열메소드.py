# 문자열.메소드()
letter = "how are YOU?"

print(letter.lower())  # lower는 lowercase로 바꿔줌
print(letter.upper())  # upper는 uppercase로 바꿔줌
print(letter.capitalize())  # capitalize는 첫글자만 대문자
print(letter.title())  # title은 마치 신문 기사 제목처럼 만들어줌
print(letter.swapcase())  # swapcase 대소문자를 뒤바꾸려면
print(letter.split())  # 문자열을 나누고 싶다면 split
print(letter.count("how"))  # 특정단어의 수
